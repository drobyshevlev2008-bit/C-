#include "Student.h"
#include <iostream>
#include <string>
using namespace std;

int main() {
    setlocale(LC_ALL, "Russian"); // для поддержки русского языка в консоли (Windows)

    cout << "=== Тестирование класса Student ===\n\n";

    // Создаём объект с помощью конструктора с параметрами
    Student student1("Иванов Иван Иванович", "15.05.2001", "+7-123-456-78-90",
        "Москва", "Россия", "МГУ", "Москва", "Россия", "ИУ5-21");

    cout << "Информация о студенте 1 (задана через конструктор):\n";
    student1.outputData();
    cout << "------------------------\n\n";

    // Создаём второго студента и вводим данные с клавиатуры
    Student student2;
    cout << "Введите данные для студента 2:\n";
    student2.inputData(); // здесь используется getline, поэтому перед этим не должно быть cin >>

    cout << "\nИнформация о студенте 2:\n";
    student2.outputData();
    cout << "------------------------\n\n";

    // Используем сеттеры, чтобы изменить некоторые поля первого студента
    cout << "Изменим телефон и группу у студента 1 через сеттеры:\n";
    student1.setPhone("+7-999-888-77-66");
    student1.setGroupNumber("ИУ5-22");

    cout << "Обновлённая информация о студенте 1:\n";
    student1.outputData();

    return 0;
}